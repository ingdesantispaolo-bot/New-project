export const italianVocabularyEntries: ItalianVocabularyEntry[] = vocabularyRows.map(({ category, item }, index) => {
  const [term, clue, wordClass, level] = item;
  const slug = term
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\W+/g, "-")
    .replace(/^-|-$/g, "")
    .toLowerCase();
  return {
    id: `${category}-${slug}-${index}`,
    term,
    clue,
    category,
    wordClass,
    level,
  };
});

export function italianVocabularyByMaxLevel(level: number): ItalianVocabularyEntry[] {
  const capped = Math.max(1, Math.min(8, Math.floor(level)));
  return italianVocabularyEntries.filter((entry) => entry.level <= capped);
}

export type ItalianWordClass = "nome" | "verbo" | "aggettivo" | "avverbio" | "connettivo" | "locuzione";

export type ItalianVocabularyEntry = {
  id: string;
  term: string;
  clue: string;
  category: ItalianVocabularyCategory;
  wordClass: ItalianWordClass;
  level: 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8;
};

type CompactRow = readonly [
  term: string,
  clue: string,
  wordClass: ItalianWordClass,
  level: ItalianVocabularyEntry["level"],
];

const rows = <T extends readonly CompactRow[]>(category: ItalianVocabularyCategory, items: T) =>
  items.map((item) => ({ category, item }));

type Pair = readonly [term: string, clue: string];

const topicRows = (
  category: ItalianVocabularyCategory,
  wordClass: ItalianWordClass,
  level: ItalianVocabularyEntry["level"],
  items: readonly Pair[],
) => rows(category, items.map(([term, clue]) => [term, clue, wordClass, level] as const));

